class Solution{
	public:
	int NthRoot(int n, int m)
	{
	    int l = 0;
	    while(pow(l,n)<=m){
	        l++;
	    }
	   int temp = pow(l-1,n);
	   if(temp == m ) return l-1;
	   return -1;
	}  
};

//{ Driver Code Starts.
int main(){
	int tc;
	cin >> tc;
	while(tc--){
		int n, m;
		cin >> n >> m;
		Solution ob;
		int ans = ob.NthRoot(n, m);
		cout << ans << "\n";
	}  
	return 0;
}
